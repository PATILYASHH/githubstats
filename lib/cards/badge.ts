export interface BadgeOpts {
  message: string;
  color: string;
  logo?: string;
  logoColor?: string;
}

// A flat "for-the-badge"-style pill: optional brand logo + uppercase label.
export function renderBadge(opts: BadgeOpts): string {
  const text = opts.message.toUpperCase();
  const fontSize = 11;
  const h = 28;
  const padX = 12;
  const gap = 7;
  const brand = opts.logo ? BRAND_ICONS[opts.logo] : undefined;
  const logoSize = 14;
  const spacing = 1.2;
  const tw = textWidth(text, fontSize, true) + text.length * spacing;
  const logoW = brand ? logoSize + gap : 0;
  const w = Math.ceil(padX * 2 + logoW + tw);
  const color = opts.color.replace(/^#/, "");
  const logoColor = (opts.logoColor ?? "ffffff").replace(/^#/, "");

  const logoSvg = brand
    ? `<svg x="${padX}" y="${(h - logoSize) / 2}" width="${logoSize}" height="${logoSize}" viewBox="0 0 ${brand.vb} ${brand.vb}" fill="#${logoColor}"><path d="${brand.d}"/></svg>`
    : "";
  const textX = padX + logoW;

  return `<svg xmlns="http://www.w3.org/2000/svg" width="${w}" height="${h}" viewBox="0 0 ${w} ${h}" role="img" aria-label="${escapeXml(
    opts.message
  )}">
  <rect width="${w}" height="${h}" rx="4" fill="#${color}"/>
  ${logoSvg}
  <text x="${textX}" y="${h / 2 + 4}" font-family="${FONT}" font-size="${fontSize}" font-weight="700" letter-spacing="${spacing}" fill="#ffffff">${escapeXml(
    text
  )}</text>
</svg>`;
}
